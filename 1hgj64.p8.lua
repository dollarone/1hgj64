         {rnd(2)-1, rnd(2)-1, rnd(2)-1},
         {rnd(2)-1, rnd(2)-1, rnd(2)-1},
				 {(rnd(2000)*0.01)-1, (rnd(2000)*0.01)-1, (rnd(2000)*0.01)-1.5},
         },
       {{1,2}, -- lines
        {2,3},
        {3,4},
        {4,1},
        {5,6},
        {6,7},
        {7,8},
        {8,5},
        {1,5},
        {2,6},
        {3,7},
        {4,8},
}}


function _init()
  cam = {0,0,-2.5} -- Initilise the camera position
  mult = 64 -- View multiplier
  win = false

  a = flr(rnd(3))+1 -- Angle for random rotation
  t = 0 -- Time until next angle change
  --p1 = {{{rnd(2000)*0.01)-1, rnd(2000)*0.01)-1, rnd(2000)*0.01)-1.5}}}
  --p2 = {{{rnd(2000)*0.01)-1, rnd(2000)*0.01)-1, rnd(500)*0.01)+0.5}}}
end

function _update()
  -- Handle the inputs
  if win then 
    if btn(4) and btn(5) then 
  		cube[1][9] = {rnd(2)-1, rnd(2)-1, rnd(2)-1}
      cube[1][10] = {rnd(2)-1, rnd(2)-1, rnd(2)-1}
      win = false
    end
    return 
  end
  if btn(0) then cube = rotate_shape(cube,1,-0.01) end --cam[1] -= 0.1 end
  if btn(1) then cube = rotate_shape(cube,1,0.01) end --cam[1] += 0.1 end
  if btn(2) then cube = rotate_shape(cube,2,-0.01) end
  if btn(3) then cube = rotate_shape(cube,2,0.01) end
  if btn(4) then cube = rotate_shape(cube,3,-0.01) end
  if btn(5) then cube = rotate_shape(cube,3,0.01) end
  t += 0.03 -- Decrease time until next angle change
end

function _draw()
  cls() -- Clear the screen
  print("make the points overlap!",0,6*0) -- Print time until angle change
  print("time units spent: "..flr(t),0,6*19) -- Print time until angle change
	print("controls: up,down,left,right,z,x",0,6*20) -- Print time until angle change
	p1 = cube[1][9]
	p2 = cube[1][10]
  x0, y0 = project(p1) --project(cube[1][10])
  x1, y1 = project(p2) --project(cube[1][12])

  print("point1 projected at: "..flr(x0)..","..flr(y0),0,6*1)
  print("point2 projected at: "..flr(x1)..","..flr(y1),0,6*2)
  draw_shape(cube) -- Draw the cube
  draw_point(p1,15)
  draw_point(p2,8)

  x0, y0 = project(p1)
  x1, y1 = project(p2)
  if (flr(x0) == flr(x1) and flr(y0) == flr(y1)) then
  	print("you win!", 0,6*6)
  	print("press z and x to restart", 0,6*8)
  	win = true
  end

end

function draw_shape(s,c)
  for l in all(s[2]) do -- For each line in the shape...
    draw_line(s[1][l[1]], s[1][l[2]], c) -- Draw the line
  end
end

function draw_line(p1,p2,c)
  x0, y0 = project(p1) -- Get the 2d location of the 3d points...
  x1, y1 = project(p2)
  line(x0, y0, x1, y1, c or 11) -- And draw a line between them
end

function draw_point(p,c)
  x, y = project(p) -- Get the 2d location of the 3d point...
  pset(x, y, c or 11) -- And draw the point
end

function project(p)
  x = (p[1]-cam[1])*mult/(p[3]-cam[3]) + 127/2 -- Calculate x and center it
  y = -(p[2]-cam[2])*mult/(p[3]-cam[3]) + 127/2 -- Calculate y and center it
  return x, y -- Return the two points
end

function translate_shape(s,t)
  ns = {{},s[2]} -- Copy the shape, but zero out the points and keep the lines
  for p in all(s[1]) do -- For each point in the original shape...
    add(ns[1],{p[1]+t[1],p[2]+t[2],p[3]+t[3]}) -- Add the displacement to the point and add it to our new shape
  end
  return ns -- Return the new shape
end

function rotate_shape(s,a,r)
  ns = {{},s[2]} -- Copy the shape, but zero out the points and keep the lines
  for p in all(s[1]) do -- For each point in the original shape...
    add(ns[1], rotate_point(p,a,r)) -- Rotate the point and add it to the new shape
  end
  return ns -- Return the new shape
end

function rotate_point(p,a,r)
  -- Figure out which axis we're rotating on
  if a==1 then
    x,y,z = 3,2,1
  elseif a==2 then
    x,y,z = 1,3,2
  elseif a==3 then
    x,y,z = 1,2,3
  end
  _x = cos(r)*(p[x]) - sin(r) * (p[y]) -- Calculate the new x location
  _y = sin(r)*(p[x]) + cos(r) * (p[y]) -- Calculate the new y location
  np = {} -- Make new point and assign the new x and y to the correct axes
  np[x] = _x
  np[y] = _y
  np[z] = p[z]
  return np -- Return new point
end 
